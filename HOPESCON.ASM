	INT 21H
	MOV DL,0AH
	INT 21H
	;------DISPLAY STRING LOGINMSG
	MOV AH,09H
	LEA DX,LOGINMSG
	INT 21H
	;------DISPLAY STRING TRIAL MSG AND LOGINCOUNT
	MOV AH,09H
	LEA DX,TRIALMSG
	INT 21H
	MOV AL,TRIAL
	ADD LOGINCOUNT,30H
	SUB AL,LOGINCOUNT
	ADD AL,30H
	SUB LOGINCOUNT,30H
	MOV AH,02H
	MOV DL,AL
	INT 21H
	MOV AH,02H
	MOV DL,29H
	INT 21H
	;------NEWLINE
	MOV AH,02H
	MOV DL,0DH
	INT 21H
	MOV DL,0AH
	INT 21H
