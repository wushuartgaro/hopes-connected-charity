	LEA DX,USERNAME
	INT 21H	
	CMP USERNAMEDATA[0],0DH
	JNE NOEXIT
	JMP EXIT
	NOEXIT:
	;------NEWLINE
	MOV AH,02H
	MOV DL,0DH
	INT 21H
	MOV DL,0AH
	INT 21H
		
	INPUTPASSWORD:
	;------DISPLAY STRING PASSWORDMSG
	MOV AH,09H
	LEA DX,PASSWORDMSG
	INT 21H

	MOV CX,5
	MOV SI,0
	CLEARPASSWORD:
		MOV AL,dollar
		MOV PASSWORD[SI],AL
		INC SI 
		LOOP CLEARPASSWORD
	MOV SI,0
	PASSWORDINPUT:
		MOV AH,01H
		INT 21H
		CMP AL,0DH
		JE VALIDATE
		CMP AL,08H
		JE BACKSPACE
		MOV PASSWORD[SI],AL
		MOV AH,02H
		MOV DL,08H
		INT 21H
		MOV DL,ASTERISK 
		INT 21H
	nextPassword:
		INC SI
		CMP SI,6
		JE REJECT
		JMP PASSWORDINPUT
	BACKSPACE:
		MOV AH,02H
		MOV DL,20H
		INT 21H
		MOV DL,08H
		INT 21H
		MOV AL,DOLLAR
		MOV PASSWORD[SI-1],AL
		CMP SI,0
		JE SKIPSI
		DEC SI
		SKIPSI:
		JMP PASSWORDINPUT
	REJECT:
		MOV AH,02H
		MOV DL,08H
		INT 21H
		MOV DL,20H
		INT 21H
		MOV DL,08H
		INT 21H
		DEC SI
		MOV AL,DOLLAR
		MOV PASSWORD[SI],AL
		JMP PASSWORDINPUT
	CENTER1:
		;------NEWLINE
		MOV AH,02H
		MOV DL,0DH
		INT 21H
		MOV DL,0AH
		INT 21H
		MOV AH,09
		LEA DX,CONTINUEMSG
		INT 21H
		MOV AH,01H
		INT 21H
		JMP LOGIN
	VALIDATE:
	;------VALIDATE USERNAME
	MOV SI,0
	MOV DI,0
	JMP VALIDATEUSERNAME
	
	VALIDATEUSERNAME:
	MOV AL,DEFAULTUSERNAME[SI]
	CMP AL,USERNAMEDATA[SI]
	JE NEXTNAMECHK
	JNE INVALIDNAME
	
	NEXTNAMECHK:
	INC SI
	CMP SI,3
	JE VALIDATEPASSWORD
	JNE VALIDATEUSERNAME	

